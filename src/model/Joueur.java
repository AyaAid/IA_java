package model;

public class Joueur {

    private String joueur;
    private String couleur;


    public Joueur(String joueur, String couleur) {
        this.joueur = joueur;
        this.couleur = couleur;
    }

    public String getJoueur() {
        return joueur;
    }

    public String getCouleur() {
        return couleur;
        
    }


}